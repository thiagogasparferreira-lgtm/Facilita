function getLanguage() {
  return localStorage.getItem('facilita_lang') || 'pt';
}

function setLanguage(lang) {
  if (translations[lang]) {
    localStorage.setItem('facilita_lang', lang);
    applyTranslations();
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: lang }));
  }
}

function applyTranslations() {
  const lang = getLanguage();
  const dict = translations[lang];
  
  // Set html lang attribute
  document.documentElement.setAttribute('lang', lang);

  // Apply to text elements
  document.querySelectorAll('[data-i18n]').forEach(elem => {
    const key = elem.getAttribute('data-i18n');
    if (dict[key]) {
      elem.textContent = dict[key];
    }
  });

  // Apply to inputs
  document.querySelectorAll('[data-i18n-placeholder]').forEach(elem => {
    const key = elem.getAttribute('data-i18n-placeholder');
    if (dict[key]) {
      elem.setAttribute('placeholder', dict[key]);
    }
  });
}

document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
  
  // Expose internationally active tools switcher trigger
  const langSelectors = document.querySelectorAll('.lang-select');
  if (langSelectors) {
    langSelectors.forEach(select => {
      select.value = getLanguage();
      select.addEventListener('change', (e) => {
        setLanguage(e.target.value);
      });
    });
  }
});
